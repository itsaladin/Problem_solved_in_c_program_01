#include <stdio.h>

int main()
{
   int m=4,n=3;

   if(m%2==0){
    printf("%d is even number \n",m);
   }else{
       printf("%d is odd number \n",m);
   }

   if(n%2==0){
    printf("%d is even number \n",n);
   }else{
       printf("%d is odd number \n",n);
   }

   return 0;
}
